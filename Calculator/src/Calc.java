public class Calc {
    public static int minus (int a, int b) {
        return a - b;
    }

    public static int add (int a, int b) {
        return a + b;
    }

    public static int divide (int a, int b) {
        return a / b;
    }

    public static int multi (int a, int b) {
        return a * b;
    }

    public static int sqPerimeter (int sideLength) {
        return sideLength * 4;
    }
}
